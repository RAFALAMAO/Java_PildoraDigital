public class DeclaracionesOperaciones {
  public static void main(String[] args) {
    // Lógicos
    // > mayor que
    // < menor que
    // <> mayor o menor que
    // >= mayor o igual que
    // <= menor o igual que
    // == igual que
    // != no igual que

    // Concatenación
    // +

    // Ejemplos
    int a = 5;
    int b;
    b = 7;

    int c = a + b;
    c++;
    c+=2;

    int div = c / 2; // Si la divicion tiene decimales, el resultado es un entero solamente

    double a1 = 5;
    double b1 = 10;
    double div2 = (a1 + b1) / 2.0;

    System.out.println(c);
    System.out.println(div);
    System.out.println(div2);
  }
}
